package com.agri.dto;

/**
 *
 * @author ANKITA-PC
 */
public class Editor {
    
    private String editorId;
    private String editor_firstName;
    private String editor_lastName;
    private String editor_email;
    private String editor_phoneNo;

    /**
     * @return the editorId
     */
    public String getEditorId() {
        return editorId;
    }

    /**
     * @param editorId the editorId to set
     */
    public void setEditorId(String editorId) {
        this.editorId = editorId;
    }

    /**
     * @return the editor_firstName
     */
    public String getEditor_firstName() {
        return editor_firstName;
    }

    /**
     * @param editor_firstName the editor_firstName to set
     */
    public void setEditor_firstName(String editor_firstName) {
        this.editor_firstName = editor_firstName;
    }

    /**
     * @return the editor_lastName
     */
    public String getEditor_lastName() {
        return editor_lastName;
    }

    /**
     * @param editor_lastName the editor_lastName to set
     */
    public void setEditor_lastName(String editor_lastName) {
        this.editor_lastName = editor_lastName;
    }

    /**
     * @return the editor_email
     */
    public String getEditor_email() {
        return editor_email;
    }

    /**
     * @param editor_email the editor_email to set
     */
    public void setEditor_email(String editor_email) {
        this.editor_email = editor_email;
    }

    /**
     * @return the editor_phoneNo
     */
    public String getEditor_phoneNo() {
        return editor_phoneNo;
    }

    /**
     * @param editor_phoneNo the editor_phoneNo to set
     */
    public void setEditor_phoneNo(String editor_phoneNo) {
        this.editor_phoneNo = editor_phoneNo;
    }
    
}

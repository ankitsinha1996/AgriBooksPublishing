package com.agri.dto;

/**
 *
 * @author ANKITA-PC
 */
public class Designer {
    
    private String designerId;
    private String designer_firstName;
    private String designer_lastName;
    private String designer_email;
    private String designer_phoneNo;

    /**
     * @return the designerId
     */
    public String getDesignerId() {
        return designerId;
    }

    /**
     * @param designerId the designerId to set
     */
    public void setDesignerId(String designerId) {
        this.designerId = designerId;
    }

    /**
     * @return the designer_firstName
     */
    public String getDesigner_firstName() {
        return designer_firstName;
    }

    /**
     * @param designer_firstName the designer_firstName to set
     */
    public void setDesigner_firstName(String designer_firstName) {
        this.designer_firstName = designer_firstName;
    }

    /**
     * @return the designer_lastName
     */
    public String getDesigner_lastName() {
        return designer_lastName;
    }

    /**
     * @param designer_lastName the designer_lastName to set
     */
    public void setDesigner_lastName(String designer_lastName) {
        this.designer_lastName = designer_lastName;
    }

    /**
     * @return the designer_email
     */
    public String getDesigner_email() {
        return designer_email;
    }

    /**
     * @param designer_email the designer_email to set
     */
    public void setDesigner_email(String designer_email) {
        this.designer_email = designer_email;
    }

    /**
     * @return the designer_phoneNo
     */
    public String getDesigner_phoneNo() {
        return designer_phoneNo;
    }

    /**
     * @param designer_phoneNo the designer_phoneNo to set
     */
    public void setDesigner_phoneNo(String designer_phoneNo) {
        this.designer_phoneNo = designer_phoneNo;
    }
    
}

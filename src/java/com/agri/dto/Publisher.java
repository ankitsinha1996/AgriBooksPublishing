package com.agri.dto;

/**
 *
 * @author ANKITA-PC
 */
public class Publisher {
    
    private String publisherId;
    private String publisher_firstName;
    private String publisher_lastName;
    private String publisher_email;
    private String publisher_phoneNo;

    /**
     * @return the publisherId
     */
    public String getPublisherId() {
        return publisherId;
    }

    /**
     * @param publisherId the publisherId to set
     */
    public void setPublisherId(String publisherId) {
        this.publisherId = publisherId;
    }

    /**
     * @return the publisher_firstName
     */
    public String getPublisher_firstName() {
        return publisher_firstName;
    }

    /**
     * @param publisher_firstName the publisher_firstName to set
     */
    public void setPublisher_firstName(String publisher_firstName) {
        this.publisher_firstName = publisher_firstName;
    }

    /**
     * @return the publisher_lastName
     */
    public String getPublisher_lastName() {
        return publisher_lastName;
    }

    /**
     * @param publisher_lastName the publisher_lastName to set
     */
    public void setPublisher_lastName(String publisher_lastName) {
        this.publisher_lastName = publisher_lastName;
    }

    /**
     * @return the publisher_email
     */
    public String getPublisher_email() {
        return publisher_email;
    }

    /**
     * @param publisher_email the publisher_email to set
     */
    public void setPublisher_email(String publisher_email) {
        this.publisher_email = publisher_email;
    }

    /**
     * @return the publisher_phoneNo
     */
    public String getPublisher_phoneNo() {
        return publisher_phoneNo;
    }

    /**
     * @param publisher_phoneNo the publisher_phoneNo to set
     */
    public void setPublisher_phoneNo(String publisher_phoneNo) {
        this.publisher_phoneNo = publisher_phoneNo;
    }
    
    
}
